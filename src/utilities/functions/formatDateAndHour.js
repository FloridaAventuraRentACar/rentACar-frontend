export function formatDateAndHour(dateString) {
    if (!dateString) return "";
    const date = new Date(dateString)
    return date.toLocaleDateString("es-ES", {
      year: "numeric",
      month: "long",
      day: "numeric",
      hour: "2-digit",
      minute: "2-digit",
    })
  }

export default formatDateAndHour